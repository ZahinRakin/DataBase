CREATE TABLE buy (
    tid NUMBER(10) PRIMARY KEY,
    cid NUMBER(10),
    isbn NUMBER(30),
    year NUMBER(4),
    month NUMBER(2),
    day NUMBER(2),
    CONSTRAINT fk_buy_cid FOREIGN KEY(cid) 
    REFERENCES customer(cid),
    CONSTRAINT fk_buy_isbn FOREIGN KEY(isbn) 
    REFERENCES book(isbn)
);


